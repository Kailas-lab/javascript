
//Without parameters:
const greet = () => console.log("Hello, World!");
greet(); // Outputs: Hello, World!

//With parameters:
const multiply = (a, b) => a * b;
console.log(multiply(2, 5)); // Outputs: 10


//With multiple statements: 
const sum = (a, b) => {
    const result = a + b;
    return result;
  };
  console.log(sum(3, 4)); // Outputs: 7
  